#include <iostream>
using namespace std;

int main() {
    int a1 = 10, a2 = 20, a3 = 30, a4 = 40;
    int b1 = 2, b2 = 3, b3 = 4, b4 = 5;
    a1 += b1; // a1 = a1 + b1
    a2 -= b2; // a2 = a2 - b2
    a3 *= b3; // a3 = a3 * b3
    a4 /= b4; // a4 = a4 / b4
    cout << "a1 = " << a1 << endl;
    cout << "a2 = " << a2 << endl;  
    cout << "a3 = " << a3 << endl;
    cout << "a4 = " << a4 << endl;
    return 0;
}