// bool
#include <iostream>
using namespace std;

int main() {
    bool b1 = true;
    bool b2 = false;
    cout << "b1: " << b1 << endl; // Outputs: b1: 1
    cout << "b2: " << b2 << endl; // Outputs: b2: 0
    cout << "sizeof(bool): " << sizeof(bool) << endl; // Outputs: sizeof(bool): 1
    return 0;
}