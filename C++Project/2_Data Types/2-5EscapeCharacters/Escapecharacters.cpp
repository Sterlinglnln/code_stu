// Escape Characters
#include <iostream>
using namespace std;

int main() {
    cout << "Hello, World!" << endl; // New line
    cout << "aaa\tTabbed text" << endl; // Tab character
    cout << "a\tTabbed text" << endl;
    cout << "\\" << endl; // Backslash
    cout << "\"" << endl; // Double quote
    cout << "\'" << endl; // Single quote
    cout << "\rCarriage return" << endl; // Carriage return
    cout << "\fForm feed" << endl; // Form feed
    return 0;
}